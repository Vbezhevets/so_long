#include "libft.h"

char	*ft_strrchr( char *str, int c)
{
	char	*p;

	c = (unsigned char)c;
	p = (char *) str;
	while (*p)
		p++;
	while (*p != c && p > str)
		p--;
	if (p == str && *p != c)
		return (0);
	return (p);
}
