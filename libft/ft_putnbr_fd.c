#include "libft.h"

void	ft_putnbr_fd(int n, int fd)

{
	char	m;
	char	c;

	if (n == -2147483648)
	{
		write (fd, "-2147483648", 11);
		return ;
	}
	if (n < 0 && n != -2147483648)
	{
		m = '-';
		write(fd, &m, 1);
		n = -n;
	}
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	c = '0' + n % 10;
	if (fd != -1)
		write(fd, &c, 1);
}
